        private void LoadEmpList()
        {
            try
            {
                DataTable dtEmpList = new EmployeeInformation().GetEmpListFromViewList(_company.Id);

                RadGridEmpList.DataSource = dtEmpList;
                RadGridEmpList.DataBind();

                if (!IsValidUpdateForUser())
                {
                    RadGridEmpList.MasterTableView.GetColumn("colEdit").Display = false;
                }
            }
            catch (Exception ex)
            {
                Alert.Show("Something is going wrong to load employee grid" + ex);
            }



        }

        protected void Page_Load(object sender, EventArgs e)
        {
            if (!IsValidSession())
            {
                string str = Request.QueryString.ToString();
                if (str == string.Empty)
                    Response.Redirect("UserLogin.aspx?refPage=HomePage.aspx");
                else
                    Response.Redirect("UserLogin.aspx?refPage=HomePage.aspx?" + str);

            }

            if (!IsValidPageForUser())
            {
                Alert.Show("Sorry, You Don't Have permission to access this page.");
                Response.Redirect("UserLogin.aspx?refPage=HomePage.aspx", false);
            }
            if (!IsPostBack)
            {
                if (!IsValidInsertForUser())
                {
                    Alert.Show("Sorry, You Don't Have permission to access this page.");
                    Response.Redirect("ErrorPage.aspx", false);
                }
                this.LoadEmpList();
            }
        }

        protected void RadGridEmpList_OnItemCommand(object sender, GridCommandEventArgs e)
        {
            if (e.CommandName.Equals(RadGrid.FilterCommandName))
            {
                this.LoadEmpList(); ;
            }
        }

        protected void RadGridEmpList_OnPageSizeChanged(object sender, GridPageSizeChangedEventArgs e)
        {
            this.LoadEmpList();
        }

        protected void RadGridEmpList_OnPageIndexChanged(object sender, GridPageChangedEventArgs e)
        {
            this.LoadEmpList();
        }

        protected void btnEdit_OnClick(object sender, EventArgs e)
        {
            LinkButton linkedit = (LinkButton)sender;
            string[] Id = linkedit.CommandArgument.ToString().Split(';');


            Response.Redirect("EmployeeInfo.aspx?Id=" + Id[0] + "&CompanyId=" + Id[1]);
        }
    }
